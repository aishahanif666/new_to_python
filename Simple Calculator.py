num_1 = int(input("Entr 1st number: "))
opt = input("Enter operator: ")
num_2 = int(input("Enter 2nd number: "))
if opt == "+":
    print(num_1,opt,num_2,"=",num_1 + num_2)
if opt == "-":
    print(num_1,opt,num_2,"=",num_1 - num_2)
if opt == "*":
    print(num_1,opt,num_2,"=",num_1 * num_2)
if opt == "/":
    print(num_1,opt,num_2,"=",num_1 / num_2)
if opt == "**":
    print(num_1,opt,num_2,"=",num_1 ** num_2)
if opt == "%":
    print(num_1,opt,num_2,"=",num_1 % num_2)

       